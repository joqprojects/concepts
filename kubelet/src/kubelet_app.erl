%% Func: start/2
%% Returns: {ok, Pid}        |
%%          {ok, Pid, State} |
%%          {error, Reason}
%% --------------------------------------------------------------------
start(_Type, _StartArgs) ->
    {ok,LocalPort}=application:get_env(local_port),
    {ok,ServerSetUp}=application:get_env(server_setup),
    {ok,MaxInstances}=application:get_env(max_instances),
    {ok,Pid}= microservice_sup:start_link(LocalPort,ServerSetUp,MaxInstances),
    {ok,Pid}.
%% --------------------------------------------------------------------
%% Func: stop/1
%% Returns: any
%% --------------------------------------------------------------------
stop(_State) ->
    ok.

